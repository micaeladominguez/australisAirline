public class TypeSeat {

    private final String typeSeat;
    private final float priceSeat;

    public TypeSeat(String typeSeat, float priceSeat) {
        this.typeSeat = typeSeat;
        this.priceSeat = priceSeat;
    }

    public String getTypeSeat() {
        return typeSeat;
    }

    public float getPriceSeat() {
        return priceSeat;
    }
}
